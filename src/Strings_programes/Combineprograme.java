package Strings_programes;

import java.util.Scanner;

public class Combineprograme {
	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		System.out.println("enter value");
		int a=sc.nextInt();
		for(int i=0;i<=a;i++)
		{
			char ch=(char)i;
			System.out.println(ch);
		}
	}

}
